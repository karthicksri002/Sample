package firstday;

import java.util.*;

public class hashset {
	public static void main(String args[]){  
		HashSet<String> set=new HashSet<String>();  
		set.add("Yybvi");  
		set.add("Xxjay");  
		set.add("Zzvi");  
		set.add("Aaay");  
		Iterator<String> itr=set.iterator();  
		while(itr.hasNext()){  
		System.out.println(itr.next());  
		 
		} 
		
		} 

} 
